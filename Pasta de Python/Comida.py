class Comida:
    def __init__(self):
        pass

    def refeicoes(self, pratos, sobremesa):
        print ("Hoje eu comi", pratos, "de comida, e comi", sobremesa, "sobremesa(s).")

    def calorias(self, ingeridas):
        print ("Hoje eu ingeri", ingeridas, "calorias hoje.")

    def agua (self, litros):
        print ("Hoje eu bebi", litros, "litros de água.")