x = float (input ("Digite um número= "))
y = float (input ("Digite outro número= "))

soma = x+y
subtracao = x-y
multiplicacao = x*y
divisao = x/y

print ("Resultados:\n\n%.2f + %.2f = %.2f" %(x,y,soma))
print ("%.2f - %.2f = %.2f" %(x,y,subtracao))
print ("%.2f x %.2f = %.2f" %(x,y,multiplicacao))
print ("%.2f / %.2f = %.2f" %(x,y,divisao))